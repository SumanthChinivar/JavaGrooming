package basics;

public class PlusOperator {
	public static void main(String[] args) {
		System.out.println(2+3);
		System.out.println("Java"+12);
		System.out.println(123+"Java");
		System.out.println(123+"Java"+123);
		System.out.println("Java"+(123+123));
		System.out.println(true+"Java");
	}
}
